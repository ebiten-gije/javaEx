package com.javaex.basic.types;

public class VarEx {
	public static void main(String[] args) {
		int myAge;
//		주석;
		myAge = 17;
		System.out.println(myAge);
		
		int myAge2 = 18;
		System.out.println(myAge2);
		
		int v1 = 10, v2 = 20, v3 = 30;
		System.out.println(v1);
		System.out.println(v2);
		System.out.println(v3);
		
		v1 = 10;
		v2 = 10;
		v3 = 10;
		System.out.println(v1);
		
		
		
}

}
